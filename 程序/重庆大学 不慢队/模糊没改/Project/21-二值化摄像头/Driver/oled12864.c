void OLED_WrLogo(uint8 data) //画logo专用函数
{
       uint8 i=8;	
       DC_HIGH;
        asm("nop"); 
        asm("nop"); 
       D0_LOW;
        asm("nop");
        asm("nop"); 
  while(i--)
  {
    if(data&0x01)
    {
      D1_HIGH;
    }
    else
    {
      D1_LOW;
    }
    D0_HIGH;
      asm("nop");  
      asm("nop"); 
    D0_LOW;  
    data>>=1;    
  }
}


void OLED_WrDat(uint8 data, uint8 revs)
{
       uint8 i=8;	
       DC_HIGH;
        asm("nop"); 
        asm("nop"); 
       D0_LOW;
        asm("nop");
        asm("nop"); 
       if(revs) data=~data;
  while(i--)
  {
    if(data&0x80)
    {
      D1_HIGH;
    }
    else
    {
      D1_LOW;
    }
      asm("nop");  
      asm("nop"); 
    D0_HIGH;
      asm("nop");  
      asm("nop"); 
    D0_LOW;  
    data<<=1;    
  }
}
