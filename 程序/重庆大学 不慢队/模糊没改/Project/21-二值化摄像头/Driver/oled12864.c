{
    uint8 i=8;
    DC_LOW;
    asm("nop"); 
    asm("nop"); 
    D0_LOW;   
    asm("nop"); 
    asm("nop"); 
  while(i--)
  {
    if(cmd&0x80)
    {
      D1_HIGH;
    }
    else
    {
     D1_LOW; 
    }
    D0_HIGH;
    asm("nop");   
    asm("nop");  
    D0_LOW;  
    cmd<<=1;   
  } 	
}
